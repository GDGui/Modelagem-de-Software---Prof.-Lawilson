   public void listarFilmes() {
    try {
        java.sql.Connection con = ConexaoBD.obtemConexao();
        String sql = "SELECT * FROM tb_filme ORDER BY titulo ASC"; // já ordena pelo título
        java.sql.PreparedStatement stmt = con.prepareStatement(sql);
        java.sql.ResultSet rs = stmt.executeQuery();

        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        model.addColumn("ID");
        model.addColumn("Título");
        model.addColumn("Autor");
        model.addColumn("Ano");
        model.addColumn("Sinopse");
        model.addColumn("Categoria");
        model.addColumn("Usuário");

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("titulo"),
                rs.getString("autor"),
                rs.getString("ano"),
                rs.getString("categoria"),
                rs.getString("usuario_cadastro")
            });
        }

        javax.swing.JTable tabela = new javax.swing.JTable(model);

        tabela.getColumnModel().getColumn(0).setPreferredWidth(50);
        tabela.getColumnModel().getColumn(1).setPreferredWidth(200);
        tabela.getColumnModel().getColumn(2).setPreferredWidth(150);
        tabela.getColumnModel().getColumn(3).setPreferredWidth(230);
        tabela.getColumnModel().getColumn(4).setPreferredWidth(180);

        javax.swing.JScrollPane scrollPane = new javax.swing.JScrollPane(tabela);

        javax.swing.JPanel panel = new javax.swing.JPanel();
        panel.setLayout(new java.awt.BorderLayout());
        panel.add(scrollPane, java.awt.BorderLayout.CENTER);

        javax.swing.JButton btnRemover = new javax.swing.JButton("Remover Filme");

        btnRemover.addActionListener(e -> {
            int selectedRow = tabela.getSelectedRow();
            if (selectedRow >= 0) {
                int confirm = javax.swing.JOptionPane.showConfirmDialog(
                        panel,
                        "Tem certeza que deseja remover este filme?",
                        "Confirmar remoção",
                        javax.swing.JOptionPane.YES_NO_OPTION
                );

                if (confirm == javax.swing.JOptionPane.YES_OPTION) {
                    int idFilme = (int) model.getValueAt(selectedRow, 0);

                    try {
                        java.sql.Connection con2 = ConexaoBD.obtemConexao();
                        String deleteSQL = "DELETE FROM tb_filme WHERE id = ?";
                        java.sql.PreparedStatement deleteStmt = con2.prepareStatement(deleteSQL);
                        deleteStmt.setInt(1, idFilme);
                        deleteStmt.executeUpdate();

                        model.removeRow(selectedRow);

                        javax.swing.JOptionPane.showMessageDialog(panel, "Filme removido com sucesso!");

                        deleteStmt.close();
                        con2.close();
                    } catch (Exception ex) {
                        ex.printStackTrace();
                        javax.swing.JOptionPane.showMessageDialog(panel, "Erro ao remover filme: " + ex.getMessage());
                    }
                }
            } else {
                javax.swing.JOptionPane.showMessageDialog(panel, "Selecione um filme para remover.");
            }
        });

        // Adiciona listener de duplo clique
        tabela.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent e) {
                if (e.getClickCount() == 2) {
                    int selectedRow = tabela.getSelectedRow();
                    if (selectedRow >= 0) {
                        int idFilme = (int) model.getValueAt(selectedRow, 0);
                        String titulo = (String) model.getValueAt(selectedRow, 1);
                        String autor = (String) model.getValueAt(selectedRow, 2);
                        String sinopse = (String) model.getValueAt(selectedRow, 3);
                        String ano = (String) model.getValueAt(selectedRow, 4);
                        String categoria = (String) model.getValueAt(selectedRow, 5);
                        String usuario = (String) model.getValueAt(selectedRow, 6);

                        editarFilme(idFilme, titulo, autor, ano, categoria, sinopse, usuario, model, selectedRow);
                    }
                }
            }
        });

        javax.swing.JPanel southPanel = new javax.swing.JPanel();
        southPanel.add(btnRemover);

        javax.swing.JDialog dialog = new javax.swing.JDialog(this, "Lista de Filmes", true);
        dialog.setLayout(new java.awt.BorderLayout());
        dialog.add(panel, java.awt.BorderLayout.CENTER);
        dialog.add(southPanel, java.awt.BorderLayout.SOUTH);
        dialog.setSize(700, 400);
        dialog.setLocationRelativeTo(this);
        dialog.setVisible(true);

        rs.close();
        stmt.close();
        con.close();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Erro ao listar filmes: " + e.getMessage());
    }
}
private void editarFilme(
        int idFilme, 
        String titulo, 
        String autor, 
        String ano, 
        String categoria, 
        String sinopse, 
        String usuario, 
        javax.swing.table.DefaultTableModel model, 
        int rowIndex
) {
    javax.swing.JTextField tituloField = new javax.swing.JTextField(titulo);
    javax.swing.JTextField autorField = new javax.swing.JTextField(autor);
    javax.swing.JTextField sinopseField = new javax.swing.JTextField(sinopse);
    javax.swing.JTextField usuarioField = new javax.swing.JTextField(usuario);

    javax.swing.JComboBox<String> anoComboBox = new javax.swing.JComboBox<>(new String[] { "1950", "1955", "1960", /* ...*/"2025" });
    anoComboBox.setSelectedItem(ano);
    
    javax.swing.JComboBox<String> categComboBox = new javax.swing.JComboBox<>(new String[] { "ação", "tecnologia", "aventura" });
    categComboBox.setSelectedItem(categoria);

    javax.swing.JPanel panel = new javax.swing.JPanel(new java.awt.GridLayout(0, 1));
    panel.add(new javax.swing.JLabel("Título:"));
    panel.add(tituloField);
    panel.add(new javax.swing.JLabel("Autor:"));
    panel.add(autorField);
    panel.add(new javax.swing.JLabel("Sinopse:"));
    panel.add(sinopseField);
    panel.add(new javax.swing.JLabel("Ano:"));
    panel.add(anoComboBox);
    panel.add(new javax.swing.JLabel("Categoria:"));
    panel.add(categComboBox);
    panel.add(new javax.swing.JLabel("Usuário:"));
    panel.add(usuarioField);

    int result = javax.swing.JOptionPane.showConfirmDialog(this, panel, "Editar Filme", javax.swing.JOptionPane.OK_CANCEL_OPTION, javax.swing.JOptionPane.PLAIN_MESSAGE);

    if (result == javax.swing.JOptionPane.OK_OPTION) {
        try {
            java.sql.Connection con = ConexaoBD.obtemConexao();
            String updateSQL = "UPDATE tb_filme SET titulo = ?, autor = ?, ano = ?, usuario_cadastro = ? WHERE id = ?";
            java.sql.PreparedStatement stmt = con.prepareStatement(updateSQL);

            stmt.setString(1, tituloField.getText().trim());
            stmt.setString(2, autorField.getText().trim());
            stmt.setString(3, anoComboBox.getSelectedItem().toString());
            stmt.setString(4, categComboBox.getSelectedItem().toString());
            stmt.setString(5, usuarioField.getText().trim());
            stmt.setInt(6, idFilme);

            stmt.executeUpdate();

            model.setValueAt(tituloField.getText().trim(), rowIndex, 1);
            model.setValueAt(autorField.getText().trim(), rowIndex, 2);
            model.setValueAt(sinopseField.getText().trim(), rowIndex, 3);
            model.setValueAt(anoComboBox.getSelectedItem().toString(), rowIndex, 4);
            model.setValueAt(categComboBox.getSelectedItem().toString(), rowIndex, 5);
            model.setValueAt(usuarioField.getText().trim(), rowIndex, 6);

            javax.swing.JOptionPane.showMessageDialog(this, "Filme atualizado com sucesso!");

            stmt.close();
            con.close();
        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao atualizar filme: " + e.getMessage());
        }
    }
}
    
    
   
    

