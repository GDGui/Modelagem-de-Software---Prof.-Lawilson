    private void cadastrarFilmejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarFilmejButtonActionPerformed
       // Captura os textos e remove espaços desnecessários
    String titulo = tituloTextField.getText().trim();
    String autor =  autorTextField.getText().trim();
    String sinopse = sinopseTextField1.getText().trim();
    String ano = anoComboBox1.getSelectedItem().toString();
    String categoria = categComboBox.getSelectedItem().toString();
    String usuarioCadastro = usuarioTextField.getText().trim();
    
    // Verificação básica se campos foram preenchidos
    if (titulo.isEmpty() || autor.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, preencha todos os campos (Título e Autor).");
        return;
    }

    // Cria o objeto Filme
    Filme filme = new Filme (titulo, autor, ano, categoria, sinopse, usuarioCadastro);

    // Tenta cadastrar no banco
    try {
        FilmeDAO.cadastraFilme(filme);
        JOptionPane.showMessageDialog(this, "Filme cadastrado com sucesso!");

        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao cadastrar filme: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_cadastrarFilmejButtonActionPerformed

